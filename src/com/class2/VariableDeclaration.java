package com.class2;

public class VariableDeclaration {
	public static void main(String[] args) {

		// different ways to create/declare variables;
		// 1. create variables and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable
		y = 12; // 2. assign value

		// declare 1 by 1
		int a;
		int b;
		int c;
		int d;
		int f;

		// or declare all together
		int x, m, z, w, v;

		a = 10;
		b = 20;
		//for mac= cmd+shifr+f = formating(organizing the program)
		//for windows ctrl_shift+f  = formating(organizing the program)

	}

}
