package com.class17;

public class Calculator {

	public static void main(String[] args) {

	}

	public int sum(int x, int y) {

		int sum = x + y;

		return sum;

	}

	public int sub(int x, int y) {
		int sub = x - y;
		return sub;

	}

	public int mult(int x, int y) {
		int mul = x * y;
		return mul;

	}

	public int div(int x, int y) {
		int div = x / y;
		return div;

	}

}
